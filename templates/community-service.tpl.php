<h1>Közösségi szolgálat</h1>
<hr class="my-4">
<p>Kedves Diákunk!&nbsp;<span style="line-height: 1.3em;">Mint az ismeretes számodra, 2016. január 1. után érettségi vizsgát tevő tanulóknak 50 óra közösségi szolgálatot kell teljesíteniük ahhoz, hogy megkezdhessék az érettségi vizsgát. A&nbsp;</span>közösségi szolgálat teljesítésének sikere elsősorban a diákok és az osztályfőnökök együttműködésén múlik.</p>
<p>Miért válik javadra a közösségi szolgálatban való munkálkodás? Azért, mert sokszínűen fejleszti a személyiségedet, a jó munkahely megszerzéséhez szükséges érzelmi intelligenciádat (EQ):</p>
<ul>
    <li>önkéntességre nevel,</li>
    <li>a szociális érzékenységedet fejleszti,</li>
    <li>együttérzésre tanít,</li>
    <li>az aktív állampolgár eszményére oktat,</li>
    <li>tapasztalatot szerezhetsz magadról a munka világában,</li>
    <li>tapasztalatot szerezhetsz magadtól a munka világáról.&nbsp;</li>
</ul>
<p>Az alábbiakban a közösségi szolgálattal kapcsolatos tudnivalókat és az iskola által koordinált tevékenységeket ismerheted meg.</p>
<h3>Fontos tudnivalók a közösségi szolgálat teljesítéséről:</h3>
<ul>
    <li>összesen 50 óra közösségi szolgálatot kell teljesítened,</li>
    csak olyan területen folytathatsz közösségi szolgálatot, amit feltüntettél a jelentkezési lapon, ha új területen szeretnéd kipróbálni magadat, új jelentkezési lapot kell kitöltened. A lehetséges területek:
    <ul>
        <li>egészségügyi</li>
        <li>szociális és jótékonysági</li>
        <li>oktatási</li>
        <li>kultúrális és közösségi</li>
        <li>katasztrófavédelmi</li>
        <li>környezet-és természetvédelmi</li>
        <li>az óvodáskorú, sajátos nevelési igényű gyerekekkel, tanulókkal, az idő emberekkel közös sport-és szabadidős tevékenység</li>
        <li>bűn-és balesetmegelőzési</li>
    </ul>
    <li>tanítási napokon legkevesebb 1, legfeljebb 3 órás időkeretben munkálkodhatsz; tanítási napokon kívül legkevesebb 1, legfeljebb 5 órát dolgozhatsz,</li>
    <li>törekedj &nbsp;arra, hogy lehetőleg tanítási napokon kívüli időpontokban dolgozz,</li>
    <li>ügyelj arra, hogy nehogy kifuss az időből, ezért javasoljuk, hogy elsősorban 9-11. évfolyamra tervezz,</li>
    <li>ütemezz arányosan, a tanulmányaidnak megfelelően: a 9. évfolyamra 20, a 10. évfolyamra 20, a 11. évfolyamra 10 órát,</li>
    <li>a Téged fogadó szervezettel az iskolának megállapodást kell kötnie, ezért mindenképp jelentkezz az osztályfőnöködnél vagy a koordinátor-tanárnál,</li>
    <li>a közösségi szolgálatról naplót kell vezetned, ezzel igazolod az elvégzett tevékenységet és munkaórát, a naplóról neked kell gondoskodni,</li>
    <li>javasoljuk, hogy minél több területen próbáld ki magad, sohasem tudhatod, hogy nem az álmaid hivatására találsz rá!</li>
</ul>
<p><strong>Kérjük,</strong> részletesen olvassátok el feltétlenül <a href="#" class="doc">a vonatkozó jogszabályt</a>!</p>
<p>Iskolánkban a közösségi szolgálattal kapcsolatos teendőket Valentyikné Szakál Márta intézményvezető-helyettes koordinálja.</p>
<h3>A közösségi szolgálat teljesítése 5 lépésben:</h3>
<ol>
    <li>Vegyél részt a 9. évfolyamon tartott felkészítő foglalkozáson.&nbsp;</li>
    <li>Tölts ki jelentkezési lapot, amelyet a szülőnek is alá kell írnia.</li>
    <li>Tudd meg, hogy a Téged fogadó szervezettel/intézménnyel &nbsp;az iskolának van-e <a href="#" class="pdf">érvényes együttműködési megállapodása.</a>&nbsp;(az iskola honlapján tájékozódhatsz!)&nbsp;<strong><strong>Ha nincsen</strong><span style="font-weight: normal;">, akkor szólj a koordinátornak!&nbsp;</span>Ha van ilyen</strong>, akkor írd be a közösségi szolgálati naplóba, hogy hol és mikor szeretnél munkát végezni, ezt a szülőnek alá kell írnia.&nbsp;</li>
    <li>Munkára fel! A közösségi szolgálati naplódat vidd magaddal, töltsd ki és igazoltasd az elvégzett munkát.&nbsp;</li>
    <li>Mutasd meg az osztályfőnöködnek a közösségi szolgálati naplót, aki nyilvántartja a teljesítésedet.</li>
</ol>
<p>Az iskolánkat felkereső szervezetek/intézmények ajánlatait megtalálod a közösségi szolgálat hirdetőjén (főépület - I. emelet).</p>
